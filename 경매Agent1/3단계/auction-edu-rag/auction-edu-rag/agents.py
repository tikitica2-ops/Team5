"""
agents.py

에이전트 정의 모음. 각 에이전트는 RagAgent를 컬렉션/시스템프롬프트로 구성한 것.
3단계에서는 '절차 안내' 에이전트 1개만 정의하고, 이후 단계에서 추가한다.
"""

from rag import RagAgent

# 공통 안전 지침 (교육용 포지셔닝 + 환각 억제)
_SAFETY = (
    "당신은 부동산 법원경매를 가르치는 '교육용' 도우미입니다.\n"
    "규칙:\n"
    "1) 반드시 제공된 자료에 근거해서만 답하세요. 자료에 없는 내용은 '제공된 자료에서 확인되지 않습니다'라고 말하고 추측하지 마세요.\n"
    "2) 답변 마지막에 근거가 된 조문/단계/용어를 '출처:'로 밝히세요.\n"
    "3) 특정 물건의 투자 가치나 권리분석 결론을 단정하지 마세요. 개념을 가르치고 분석 방법을 안내하는 데 그치세요.\n"
    "4) 권리분석·투자 판단이 얽힌 질문에는 '본 답변은 교육용이며 실제 입찰 전 전문가 확인이 필요합니다'를 덧붙이세요.\n"
    "5) 쉽고 단계적으로, 한국어로 설명하세요.\n"
)

PROCEDURE_SYSTEM = (
    _SAFETY
    + "\n역할: 경매의 '절차와 흐름'을 안내합니다. 신청부터 배당까지 각 단계가 무엇이고 "
      "왜 중요한지, 단계 간 순서를 명확히 설명하세요."
)

procedure_agent = RagAgent(
    name="절차 안내",
    collection_keys=["procedures", "laws"],
    system_prompt=PROCEDURE_SYSTEM,
    description="경매 신청·개시결정·배당요구·매각·대금납부·배당 등 절차와 흐름, 단계별 의미에 대한 질문",
)
